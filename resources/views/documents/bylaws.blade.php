    <a name="Article1Name"></a>
    <h3 class="font-bold text-xl">Article 1: Name</h3>
    <p class="italic">How we are known, web presence</p>
    <p>
        The name of this Organization shall be the LDraw Organization, also referred to
        as LDraw.org. The organization shall use the Internet domain ldraw.org for identity
        purposes, and has a website at the URL of http://www.ldraw.org.
    </p>

    <a name="Article2Purposes"></a>
    <h3 class="font-bold text-xl">Article 2: Purposes</h3>
    <p class="italic">The LDraw.org goals, charitable nature</p>

    <a name="Section201TheLDraworgGoals"></a>
    <h4 class="font-bold text-med">Section 2.01: The LDraw.org Goals</h4>
    <p>
    The purposes of this Organization shall be:
    </p>
    <ol class="list-disc px-6">
        <li>
            Maintain and distribute the core LDraw properties, the file format and parts
            library, as free and extendable resources for everyone's benefit, in the spirit
            of James Jessiman
        </li>
        <li>Develop and maintain the LDraw format</li>
        <li>Foster communication among people interested in virtual LEGO® building</li>
        <li>Promote the virtual LEGO hobby through online resources, events, media exposure, literature, etc.</li>
        <li>Support developers of new and innovative LDraw software</li>
        <li>
            Represent the interests of the LDraw/virtual LEGO builders' community to The LEGO
            Company, LEGO fan organizations, clubs, educators, organizers of special events,
            the CAD industry, and the like
        </li>
        <li>
            Adapt and respond to other existing and potential future LEGO CAD formats,
            whether fan developed, existing CAD formats, or formats released by The LEGO Company.
        </li>
    </ol>

    <a name="Section202NonProfitNatureandCharitablePurpose"></a>
    <h4 class="font-bold text-med">Section 2.02: Non Profit Nature and Charitable Purpose</h4>
    <p class="italic">LDraw.org is a non-profit organization.</p>
    <p>
    The property of this Organization is irrevocably dedicated to charitable purposes
    and no part of the net income or assets of the Organization shall be used to benefit
    private persons.
    </p>

    <a name="Article3IndividualMembership"></a>
    <h3 class="font-bold text-xl">Article 3: Individual Membership</h3>
    <p>
    <p class="italic">Individual members, types and qualifications, non-discrimination</p>
    </p>

    <a name="Section301ClassesofMembership"></a>
    <h4 class="font-bold text-med">Section 3.01: Classes of Membership</h4>
    <p>
    There shall be three classes of Individual Membership: Active, Fellow and Honorary.
    Where these bylaws refer to Individual Member without a distinguishing qualifier,
    the provision shall apply to all Individual Members, including Active, Fellow and Honorary.
    </p>

    <a name="Section302AcceptanceProceduresforIndividualMembership"></a>
    <h4 class="font-bold text-med">Section 3.02: Acceptance Procedures for Individual Membership</h4>
    <p>
    The procedure and application for accepting all new classes of membership shall be
    determined by the Steering Committee or sub-committee and recorded in the LDraw.org
    Policies and Procedures document.
    </p>

    <a name="Section303ActiveMembership"></a>
    <h4 class="font-bold text-med">Section 3.03: Active Membership</h4>
    <p>
    Active Membership may be granted to applicants:
    </p>
    <ol class="list-disc px-6">
    <li>Who are natural persons (individual humans, not corporations or other entities)</li>
    <li>Who are of the age of majority in their country (18 in the USA)</li>
    <li>Who positively affirm the LDraw.org goals</li>
    <li>Who fulfill one of the following requirements for identity verification as specified in the Policies and Procedures document.</li>
    </ol>
    <p>
    Active Members shall be entitled to one vote on all matters placed before the Active
    Membership. The Steering Committee may place business before the Active Members
    and hold a vote by ballot, in accordance with the provisions in these Bylaws.
    </p>

    <a name="Section304FellowMembership"></a>
    <h4 class="font-bold text-med">Section 3.04: Fellow Membership</h4>
    <p>
    Any Active Member who has pre-eminently distinguished him or herself in LEGO CAD
    shall be eligible for Fellow Membership.
    </p>
    <p>
    An Active Member must be nominated for Fellow member status by at least three members
    of the Active Membership. The Steering Committee will then consider the nomination
    and decide whether or not to present it to the Active Membership for a vote. If
    it is put to a vote, the status will be bestowed by the affirmative vote of a
    simple majority of the Active Membership. This status shall not be bestowed on
    any Active Member until at least 1 year after the date of adoption of these bylaws.
    </p>
    <p>
    Fellow Members have all rights and responsibilities of active members except shall
    be exempt from any dues and assessments.
    </p>

    <a name="Section305HonoraryMembership"></a>
    <h4 class="font-bold text-med">Section 3.05: Honorary Membership</h4>
    <p>
    Any non-LDraw.org member who has rendered meritorious services in one of LDraw.org
    designated areas of interest shall be eligible for Honorary Membership. Honorary
    members are entitled to all rights and privileges of LDraw.org except the right
    to vote or to hold office.
    </p>
    <p>
    An Honorary Member must be nominated for Honorary member status by at least three
    members of the Active Membership or Steering Committee. This status will then be
    bestowed by the affirmative vote of a simple majority of the Active Membership.
    The Steering Committee shall have the power to veto the Active Membership’s affirmation
    vote. The Active Membership shall not bestow such status on any individual until
    at least 1 year after the date of adoption of these bylaws.
    </p>

    <a name="Section306TerminationorSuspensionofIndividualMembership"></a>
    <h4 class="font-bold text-med">Section 3.06: Termination or Suspension of Individual Membership</h4>
    <p>
    An Individual Member may terminate his or her membership voluntarily (see procedure
    for this in the Policies and Procedures document). All rights, privileges, and
    interests of such member shall cease on resignation from the Membership. Resignations
    shall be effective upon fulfillment of all obligations to the date of withdrawal.
    Members may request their personal information be deleted from LDraw.org records
    upon resignation or termination for privacy reasons.
    An Individual Member may be suspended or terminated for due cause. Sufficient cause
    for such suspension or termination of Membership shall be any of the following:
    </p>
    <ol class="list-disc px-6">
    <li>Violation of the provisions of these Bylaws or any lawful rule or practice duly adopted by LDraw.org</li>
    <li>Nonpayment of dues or other financial obligations</li>
    <li>Willful misrepresentation or falsification of any material statement of fact</li>
    <li>Any other conduct prejudicial to the interests of the LDraw.org, as so identified by the Steering Committee.</li>
    </ol>
    <p>
    The Steering Committee will determine the procedures to be followed for suspension
    and termination. A former member so suspended or terminated may be reinstated by
    resolution of the Steering Committee. Particulars will be found in the Policies
    and Procedures document.
    </p>

    <a name="Section307IndividualMembershipPolicy"></a>
    <h4 class="font-bold text-med">Section 3.07: Individual Membership Policy</h4>
    <p>
    It is the policy of LDraw.org not to discriminate in its qualifications for membership
    against any person by reason of his or her sex, race, religion, creed, sexual
    orientation, or other physical condition, or national origin.
    </p>

    <a name="Article4Dues"></a>
    <h3 class="font-bold text-xl">Article 4: Dues</h3>
    <p>
    <p class="italic">Dues, the levy and payment thereof</p>
    </p>

    <a name="Section401FeesLeviedAgainsttheMembershipClasses"></a>
    <h4 class="font-bold text-med">Section 4.01: Fees Levied Against the Membership Classes</h4>
    <p>
    Any fees levied against any or all classes of membership, including changes in
    annual dues, shall be proposed by the Steering Committee and presented to the
    membership classes affected.
    <ol class="list-disc px-6">
    <li>For changes to annual dues the Steering Committee shall present the proposal for a 30 day public discussion period.</li>
    <li>
        For all other fees, not including changes to annual dues, the Steering Committee
        shall present the proposal for public discussion as soon as the Steering Committee
        is aware of the need for a fee. In any case, the public discussion period shall
        not be less than 12 days.
    </li>
    </ol>
    <p>
    At the conclusion of the public discussion period, all eligible members of the
    affected membership classes who do not wish to abstain shall vote on the ratification
    of the fee proposal. A measure shall pass by a simple majority of votes cast in a poll.
    </p>

    <a name="Section402SuspensionofMembership"></a>
    <h4 class="font-bold text-med">Section 4.02: Suspension of Membership</h4>
    <p>
    Any Individual Member behind in dues, after two dues notices having been sent,
    shall have his or her membership susupended 90 days after the date of the second
    notice. Records, electronic or otherwise, covering suspended members shall be kept
    on file in case the member seeks reinstatement in the future.
    </p>
    <p>
    A Member may be reinstated upon payment of the annual dues in effect at the time
    of reinstatement. Notices sent to last known email address are deemed sufficient.
    </p>

    <a name="Article5MeetingsoftheMembership"></a>
    <h3 class="font-bold text-xl">Article 5: Meetings of the Membership</h3>
    <p>
    <p class="italic">LDraw.org is an electronic organization, the mailing list, and convention particulars</p>
    </p>

    <a name="Section501ElectronicCommunication"></a>
    <h4 class="font-bold text-med">Section 5.01: Electronic Communication</h4>
    <p>
    LDraw.org is an electronic facilitated organization; and as such, physical meetings
    will not be required. Meetings of the LDraw.org Steering Committee, and other
    formed sub-committees, will be conducted via electronic mailing lists. A notice
    sent by electronic mail to a member’s last known email address shall be deemed
    sufficient.
    </p>
    <p>
    Announcements and public records shall be made available on the LDraw.org web pages.
    The organization shall also ensure a system is in place to email notices to members
    upon request. What constitutes "sufficient notice" of announcements and public records
    can be found in the Policies and Procedures document.
    </p>

    <a name="Section502PhysicalMeetings"></a>
    <h4 class="font-bold text-med">Section 5.02: Physical Meetings</h4>
    <p>
    A physical meeting is defined as any meeting with the intent to carry out official
    LDraw.org business that is not conducted via electronic mailing lists. Since LDraw.org
    is an electronic organization, physical meetings are discouraged but the occasional
    need for these physical meetings is recognized.
    </p>
    <p>
    The Steering Committee, LDraw.org Standards Committee, and members of a Special
    Interest Group may conduct a physical meeting but, due to the potential size, physical
    meetings or a physical quorum of the Active Membership are not recognized.
    </p>
    <p>
    Physical meetings can be held in any form that all the participants agree to,
    and as long as there is a permanent record of the discussion. The permanent record
    may in form of meeting minutes or a transcript of the meeting.
    </p>

    <a name="Section503Convention"></a>
    <h4 class="font-bold text-med">Section 5.03: Convention</h4>
    <p>
    An LDraw.org annual convention may be established by LDraw.org as membership numbers
    and interests deem possible and as the Steering Committee shall direct. In the
    interim, LDraw.org update meetings may be held at other LDraw.org related conventions
    or gatherings as convenient and as the Steering Committee shall direct.
    </p>

    <a name="Article6SteeringCommitteeMembers"></a>
    <h3 class="font-bold text-xl">Article 6: Steering Committee Members</h3>
    <p class="italic">Number of Officers, Election, Vacancy and Succession, Appointed Officers</p>

    <a name="Section601NumberofMembersandTermofOffice"></a>
    <h4 class="font-bold text-med">Section 6.01: Number of Members and Term of Office</h4>
    <p>
    There shall be five (5) elected officers of LDraw.org. These five officers shall
    collectively be known as the Steering Committee Members, and shall collectively
    constitute the Steering Committee.
    All Steering Committee Members shall have a term of office of (1) year.
    </p>

    <a name="Section602EligibilityforLEGOCompanyEmployees"></a>
    <h4 class="font-bold text-med">Section 6.02: Eligibility for LEGO Company Employees</h4>
    <p>
    Because of potential conflicts of interest, employees of the LEGO Company, their
    subsidiaries and strategic partners shall be subject to a special set of requirements
    to be eligible for service on the Steering Committee.
    </p>

    <a name="Subsection602aFullPublicDisclosure"></a>
    <h5 class="font-bold text-sm">Subsection 6.02(a) Full Public Disclosure</h5>
    <p>
    Upon nomination, the individual’s employment status shall be publicly declared.
    Employment by the LEGO Company does not preclude nomination, provided full and
    up front disclosure of status is given to the community through accepted channels
    (ie. common discussion groups, LDraw.org) where nominations are made.
    </p>
    <p>
    All official public postings listing nominees, as well as the final ballot, shall
    attach a footnote or other clearly visible reference to the nominee's name, indicating
    the person is employed by the LEGO Company. This reference shall list their title,
    and briefly describing the responsibilities and powers the position holds. The
    information page on the LDraw.org web site listing Steering Committee members once
    elected shall also include the aforementioned references.
    </p>

    <a name="Subsection602bAccountabilityandReviewProcess"></a>
    <h5 class="font-bold text-sm">Subsection 6.02(b) Accountability and Review Process</h5>
    <p>
    Employess of the LEGO Company elected to the Steering Committee shall be held to
    a different standard than Steering Committee members not employed by LEGO.
    </p>
    <p>
    Should impropriety on the part of a LEGO employee serving on the Steering Committee
    be suspected, fellow members of the Steering Committee shall by no less than unanimous
    decision be authorized to conduct a review of said employee's actions directly
    relating to LDraw.org business. The employee shall be given due process and the
    opportunity to defend his or her actions. If the review panel deems impropriety
    they may by no less than unanimous decision report their findings to the Membership
    via accepted channels. The Membership may then move to remove the officer in question
    using the removal provisions in Section 6.05.
    </p>
    <p>
    Specific procedures relating to the Accountability and Review Processes shall be
    determined by the first elected Steering Committee, and documented in the Policies
    and Procedures document.
    </p>

    <a name="Section603NominationandElectionProcessforSteeringCommitteeMembers"></a>
    <h4 class="font-bold text-med">Section 6.03: Nomination and Election Process for Steering Committee Members</h4>
    <p>
    Steering Committee Members shall be democratically elected.
    </p>
    <p>
    Elections shall be held annually, via electronic ballot.
    </p>
    <p>
    All Steering Committee Members shall enter into the performance of their elected
    duties 30 days after the election and shall continue in office until their successors
    shall be duly elected and qualified, unless they resign, are removed, or are otherwise
    unable to fulfill the term.
    </p>

    <a name="Subsection603aNominationProcess"></a>
    <h5 class="font-bold text-sm">Subsection 6.03(a) Nomination Process</h5>
    <p>
    Nominations for the 5 offices shall open no less than 2 months prior to the scheduled
    election of officers. Nominations must be made public on the electronic communication
    mechanism then in use by the Organization. A nomination shall consist of a name
    of the Individual Member and the affirmation of at least two Members (the Nominator
    and the Second). Optionally a supporting statement may be included.
    </p>
    <p>
    Nominated individuals must accept their nomination in order to appear on the ballot.
    If the individual does not respond to the nomination publicly, on the electronic
    communication mechanism then in use by the Organization, it is assumed he or she
    declines nomination. Nominations for officers shall be deemed closed just prior
    to the commencement of the election process.
    </p>

    <a name="Subsection603bElectionProcess"></a>
    <h5 class="font-bold text-sm">Subsection 6.03(b) Election Process</h5>
    <p>
    The officers of LDraw.org shall be elected annually on or before 1 March of the year.
    </p>
    <p>
    The ballot shall be secret. The Steering Committee shall determine details of the
    balloting process and make them known to the Membership at least one week in advance
    of the election. Balloting process for the first Steering Committee shall be determined
    by the Ad-Hoc Organizing Committee and made known to the Membership at least one
    week in advance of the election.
    </p>

    <a name="Subsection603cVotesRequired"></a>
    <h5 class="font-bold text-sm">Subsection 6.03(c) Votes Required</h5>
    <p>
    Steering Committee Members shall be elected by popular ballot. All Active Members
    in good standing as of the commencement of the election shall be eligible to vote.
    Each member can select up to 5 candidates on their ballot. The top 5 vote getters
    in the Steering Committee Member election shall be deemed to be elected. In the
    event of a tie in which it is not clear who the top 5 vote getters are, a runoff
    election shall be held for whatever number of committee members were not clearly
    elected, with the candidates restricted to those who tied for votes, and the number
    of selectable candidates shall be the number of tied positions. See the Policies
    and Procedures document for examples.
    </p>

    <a name="Subsection603dQualificationProcess"></a>
    <h5 class="font-bold text-sm">Subsection 6.03(d) Qualification Process</h5>
    <p>
    30 days after the election, the five newly elected Steering Committee Members shall
    be deemed to be qualified and as a group, shall now constitute the new Steering
    Committee, which shall supplant the old one.
    </p>

    <a name="Subsection603eAssumptionofOffice"></a>
    <h5 class="font-bold text-sm">Subsection 6.03(e) Assumption of Office</h5>
    <p>
    The outgoing Steering Committee shall give every aid and advice to the incoming
    Steering Committee during the transition period between election and assumption
    of office.
    </p>

    <a name="Section604RolesofSteeringCommitteeMembers"></a>
    <h4 class="font-bold text-med">Section 6.04: Roles of Steering Committee Members</h4>
    <p>
    Once elected, the Steering Committee Members shall vote amongst themselves to determine
    a chairperson, secretary, and treasurer.
    </p>

    <a name="Subsection604aDutiesoftheChairperson"></a>
    <h5 class="font-bold text-sm">Subsection 6.04(a) Duties of the Chairperson</h5>
    <p>
    The Chairperson is the primary executive officer of the organization and shall
    preside at organization meetings, should such be held. The Chairperson shall be
    the representative of the Organization on any occasion when a person is required
    to act in that capacity. The Chairperson shall make appointments and removals of
    Appointed Officers as he or she sees fit.
    </p>
    <p>
    The Chairperson may appoint a Vice Chairperson to preside in the chair's absence.
    The Vice Chairperson is the natural successor to the office of Chairperson should
    the Chairperson vacate the position for any reason.
    </p>

    <a name="Subsection604bDutiesoftheSecretary"></a>
    <h5 class="font-bold text-sm">Subsection 6.04(b) Duties of the Secretary</h5>
    <p>
    The Secretary shall be responsible for compiling and publishing a quarterly report
    of Steering Committee activities and progress.
    </p>

    <a name="Subsection604cDutiesoftheTreasurer"></a>
    <h5 class="font-bold text-sm">Subsection 6.04(c) Duties of the Treasurer</h5>
    <p>
    The Treasurer shall manage all financial transactions, and maintain a correct record
    of funds available, Organization debts outstanding, and Member debts to the Organization
    outstanding. The Treasurer shall report financial activity and status at all annual
    and monthly meetings. The Treasurer must supply the Secretary with the financial
    records for inclusion in the quarterly report.
    </p>

    <a name="Section605RemovalofOfficersfromtheSteeringCommittee"></a>
    <h4 class="font-bold text-med">Section 6.05: Removal of Officers from the Steering Committee</h4>
    <p>
    Officers may not be removed from the Steering Committee except for due cause. If
    an Officer is absent from three consecutive votes or a total of 5 votes in any
    calendar year for reasons that the Committee fails to declare sufficient, his or
    her resignation shall be deemed to have been tendered and accepted.
    </p>
    <p>
    If serious, documented allegations of impropriety on behalf of a Steering Committee
    member are made, the Active Membership may move to hold a vote to remove the offending
    individual from office. A vote will be held once fifteen percent of the Active
    Membership have called for a vote. A Steering Committee member may be removed from
    office by a simple majority of the total Active Membership.
    </p>
    <p>
    Any Officer that has been removed may not be re-elected by the Active Membership
    for a period of at least one year.
    </p>

    <a name="Section606VacancyandSuccession"></a>
    <h4 class="font-bold text-med">Section 6.06: Vacancy and Succession</h4>
    <p>
    If any of the Officers cannot fulfill his or her duties, resigns, is removed, or
    is otherwise unable to fulfill the term then a replacement shall be appointed by
    the remaining Steering Committee. This appointment shall have effect until the
    next scheduled election is held.
    </p>

    <a name="Section607AppointedOfficers"></a>
    <h4 class="font-bold text-med">Section 6.07: Appointed Officers</h4>
    <p>
    The Steering Committee may appoint Additional Officers, as it deems necessary,
    with such authority and duties as it may designate, to serve at its pleasure, from
    among the Individual Members of LDraw.org. Such Additional Officers are not themselves
    members of the Steering Committee.
    Additional Officers appointments must be approved by 3 of the 5 members of the
    Steering Committee.
    </p>

    <a name="Section608AppointedKeyPositions"></a>
    <h4 class="font-bold text-med">Section 6.08: Appointed Key Positions</h4>
    <p>
    Key Positions such as Parts Tracker Administrators, Content Managers, Help Desk
    or System Administrators are appointed by the Steering Committee from among the
    individual Members of LDraw.org. Appointments must be approved by 3 of the 5 members
    of the Steering Committee. Paying tribute to the fact that LDraw.org is a completely
    volunteer organization common courtesy is extended as a general rule and except
    for extenuating circumstances the Steering Committee shall get agreement from
    the active Key Positions about new appointments. The Steering Committee might
    delegate the appointment to the active Key Positions. It will also function as
    intermediary if members volunteer for a Key Position. There shall be at least
    three (3) appointed Key Positions for the most sensitive area: The LDraw Parts Tracker.
    </p>

    <a name="Section609RemovalofKeyPositions"></a>
    <h4 class="font-bold text-med">Section 6.09: Removal of Key Positions</h4>
    <p>
    Key Positions are accountable to the Steering Committee and might be removed if
    someone fails to fulfill his or her duties. Removals must be explicitly approved
    by 3 of the 5 members of the Steering Committee. The Steering Committee has the
    obligation to report to the active Membership the circumstances for the decision.
    </p>

    <a name="Section610VacancyandSuccessionofKeyPositions"></a>
    <h4 class="font-bold text-med">Section 6.10: Vacancy and Succession of Key Positions</h4>
    <p>
    If any of the Key Positions cannot fulfill his or her duties, resigns, or is otherwise
    unable to fulfill his or her duties, then a replacement shall be appointed by the
    Steering Committee. The Steering Committee shall consult the outgoing post about
    a succession. Consulting does not apply to Key Position removed by the Steering
    Committee. The Key Positions might appoint a successor in full autonomy in the
    situation that the Steering Committee takes no action.
    </p>

    <a name="Section611ReimbursementofMembers"></a>
    <h4 class="font-bold text-med">Section 6.11: Reimbursement of Members</h4>
    <p>
    Steering Committee Members shall not receive any compensation for their services
    as Officers, but the Steering Committee may authorize reimbursement of expenses
    incurred by any member while representing or working to benefit LDraw.org provided
    such authorization is documented and published to the Membership using the electronic
    means of communication then in use. These expenses should, whenever practicable,
    be approved by the Steering Committee prior to a Member undergoing said expense.
    If the expense is not pre-approved, the Steering Committee may consider reimbursement
    but is not obligated to grant it. If such expenses are reoccurring the authorized
    prescribe procedures may be added to the LDraw.org Policies and Procedures document.
    See the Policies and Procedures document for details.
    </p>

    <a name="Article7RepresentationofLDraw.org"></a>
    <h3 class="font-bold text-xl">Article 7: Representation of LDraw.org</h3>
    <p>
    <p class="italic">Steering Committee to approve spokespersons, Use of trademarks</p>
    </p>

    <a name="Section701RepresentationinPolicyMatters"></a>
    <h4 class="font-bold text-med">Section 7.01: Representation in Policy Matters</h4>
    <p>
    No member may represent LDraw.org in policy matters without the approval of the
    Steering Committee. There may be instances that volunteers need to represent LDraw.org
    on an ongoing or recurring basis – these cases should be outlined in the LDraw.org
    Policies and Procedures document so permission does not need to be sought at each
    occurrence.
    </p>

    <a name="Section702TrademarksandVisualRepresentation"></a>
    <h4 class="font-bold text-med">Section 7.02: Trademarks and Visual Representation</h4>
    <p>
    Use of the name, logo, seal, trademark, or other graphic representation (including
    new artwork and graphic designs) of LDraw organization by any Member, Section, or
    Committee or his/her designee, that gives appearance of representing LDraw.org
    or implies approval by LDraw.org in an official manner or capacity must have the
    prior approval of the Steering Committee.
    </p>

    <a name="Article8Amendments"></a>
    <h3 class="font-bold text-xl">Article 8: Amendments</h3>
    <p>
    <p class="italic">Process for Amendment, process applies to this article too</p>
    </p>

    <a name="Section801AmendmentofBylaws"></a>
    <h4 class="font-bold text-med">Section 8.01: Amendment of Bylaws</h4>
    <p>
    The Steering Committee may propose to alter, amend, or repeal any provision of
    these Bylaws or make any additional Bylaws. Such alteration, repeal, or addition
    shall be proposed by a representative to the Active Members and Steering Committee
    to be considered for a period of 30 days. At the end of this period an affirmative
    vote of at least two-thirds of the entire Active Membership must be achieved in
    order to ratify the alteration, repeal, or addition.
    </p>

    <a name="Section802ProceduresforAmendmentofBylaws"></a>
    <h4 class="font-bold text-med">Section 8.02: Procedures for Amendment of Bylaws</h4>
    <p>
    Amendments to this Article of the Bylaws may be made only by the same voting procedure
    described in Section 1 of this Article.
    </p>

    <a name="Article9SpecialInterestGroups"></a>

    <h3 class="font-bold text-xl">Article 9: Special Interest Groups</h3>
    <p>
    <p class="italic">LSC, other special interest groups, their creation, operation and termination</p>
    </p>
    <p>
    To further the goals of the Organization, the Organization may from time to time
    create Special Interest Groups. These groups are intended for like minded Members
    to come together for the purpose of furthering a common interest.
    </p>

    <a name="Section901Creation"></a>
    <h4 class="font-bold text-med">Section 9.01: Creation</h4>
    <p>
    An individual or group of individual Active Members may approach the Steering
    Committee with a proposal to create a Special Interest Group. The proposal shall
    define the name, purpose, membership criteria, powers, and duties of the Special
    Interest Group. The Special Interest group will come into existence upon approval
    of the proposal by the Steering Committee.
    </p>

    <a name="Section902OngoingOperations"></a>
    <h4 class="font-bold text-med">Section 9.02: Ongoing Operations</h4>
    <p>
    Each Special Interest Group shall have the powers and duties defined in the resolution
    or resolutions adopting it, and shall have a designated representative (the "SIG
    Chair") to report to the Steering Committee and to the Active Membership at least
    quarterly. Such SIG Chair serves at the pleasure of the Steering Committee Chairperson
    and is an Appointed Officer within the meaning.
    </p>
    <p>
    SIGs shall not have separate treasuries. Their expenditures, if any, shall come
    from the general Organization Treasury, and their revenues, if any, shall likewise
    accrue to the Organization Treasury.
    </p>

    <a name="Section903Dissolution"></a>
    <h4 class="font-bold text-med">Section 9.03: Dissolution</h4>
    <p>
    A Special Interest Group may be dissolved by proposal of the Steering Committee
    and by an affirmative vote of the Membership under normal voting procedures.
    </p>

    <a name="Section904LDraworgStandardsBoard"></a>
    <h4 class="font-bold text-med">Section 9.04: LDraw.org Standards Board</h4>
    <p>
    The LDraw.org Standards Board shall be construed as a Special Interest Group
    within the meaning of this article, governed under the <a href="article/382">LSB Charter</a>.
    Its existence is grandfathered and does not have to be approved by resolution.
    Like all SIGs, it is subject to the Ongoing Operations and Dissolution provisions
    of this article.
    </p>

    <a name="Article10Dissolution"></a>
    <h3 class="font-bold text-xl">Article 10: Dissolution</h3>
    <p>
    <p class="italic">The process of dissolution of LDraw.org. Should LDraw.org be dissolved, assets go to charity</p>
    </p>

    <a name="Section1001DissolutionbyResolution"></a>
    <h4 class="font-bold text-med">Section 10.01: Dissolution by Resolution</h4>
    <p>
    LDraw.org may be dissolved by Resolution. Such Resolution shall be presented by
    the Steering Committee to the Individual Members and shall require an affirmative
    vote of a three-fourths majority of the membership. The resolution shall outline
    the date for termination and the charity to which the remaining assets, if any
    shall be directed.
    </p>

    <a name="Section1002DispositionofAssets"></a>
    <h4 class="font-bold text-med">Section 10.02: Disposition of Assets</h4>
    <p>
    Upon the dissolution or winding up of LDraw.org, its assets remaining after payment,
    or provision for payment, of all debts and liabilities, shall be distributed to
    a nonprofit fund, foundation, or corporation which is organized and operated for
    charitable purposes.
    </p>

    <a name="Section1003RetentionofNameandIdentifyingMarks"></a>
    <h4 class="font-bold text-med">Section 10.03: Retention of Name and Identifying Marks</h4>
    <p>
    The name, distinctive trade dress, logo and artwork of LDraw.org are significant
    and meaningful. Therefore, no other organization shall be allowed to use the organization
    name, acronym, distinctive trade dress, logo or artwork of LDraw.org without express
    permission. This Section shall survive any subsequent dissolution of LDraw.org.
    </p>
